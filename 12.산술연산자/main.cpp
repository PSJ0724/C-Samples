#include <iostream>

/* run this program using the console pauser or add your own getch, system("pause") or input loop */

int main(int argc, char** argv) {
	int s1=20, s2=3;
	
	printf("%d+%d=%d\n", s1,s2,s1+s2);
	printf("%d-%d=%d\n", s1,s2,s1-s2);
	printf("%d*%d=%d\n", s1,s2,s1*s2);
	printf("%d/%d=%d\n", s1,s2,s1/s2);
	printf("%d%%%d=%d\n", s1,s2,s1%s2);
	return 0;
}
